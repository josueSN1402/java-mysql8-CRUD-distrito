package modelo;

public class Distrito {

    private int idDis;
    private String nomDis;
    private int estDis;

    public int getIdDis() {
        return idDis;
    }

    public void setIdDis(int idDis) {
        this.idDis = idDis;
    }

    public String getNomDis() {
        return nomDis;
    }

    public void setNomDis(String nomDis) {
        this.nomDis = nomDis;
    }

    public int getEstDis() {
        return estDis;
    }

    public void setEstDis(int estDis) {
        this.estDis = estDis;
    }
}
